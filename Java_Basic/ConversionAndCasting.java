package Java_Basic;
public class ConversionAndCasting {
    public static void main(String[] args) {
        float val = 5; // Implicit conversion OR Widening,IT is automatically performed by JVM . 
        System.out.println(val);


        int val2 = (int)5.3; // Explicit conversion  Or Narrowing In this type of conversion we loss the data.
        System.out.println(val2);
    }

}
